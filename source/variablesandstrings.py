#concatenation(combing strings)

first_name = 'John'
last_name = 'Doe'
full_name = first_name + ' ' + last_name
print(full_name)
print(full_name.upper())
print(full_name.lower())
print(full_name.title())
print(full_name.strip()) ## By default, strip() removes whitespace characters such as spaces, tabs, and newlines.